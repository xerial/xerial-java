package org.xerial.amoeba.algebra;

/**
 * a label of the intermediate relation
 * @author leo
 *
 */
public class RelationLabel 
{
	String name;

	/**
	 * @param name
	 */
	public RelationLabel(String name) {
		this.name = name;
	}

	public String getName() {
		return name;
	}
	
}




